package random

import (
	"math/rand"
)

type Tokenizer struct{}

// New returns a random alphanumeric token.
func (t Tokenizer) New() string {
	const charset = "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789"
	const length = 16
	b := make([]byte, length)
	for i := range b {
		b[i] = charset[rand.Intn(len(charset))]
	}
	return string(b)
}
